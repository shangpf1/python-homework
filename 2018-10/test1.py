# 打印 1~100之间所有奇数之和

sum = 0
for x in range(1,101):
    if x % 2 != 0:
        print(x)    #打印出所有的奇数
        sum +=x
print("1~100之间所有的奇数之和：",sum)

print("------------------------------------------")

# 打印 1~100之间所有偶数之和

sum = 0
for x in range(1,101):
    if x % 2 == 0:
        print(x)    #打印出所有的偶数
        sum +=x
print("1~100之间所有的奇数之和：",sum)
